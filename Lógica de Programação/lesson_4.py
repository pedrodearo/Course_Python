""" INT e FLOAT """

""" 
Tipo: int
int -> Número Inteiro
O tipo int representa qualquer número
positivo ou negativo. int sem sinal é considerado positivo
"""

print(10) # int positivo
print(-10) # int negativo

""" 
Tipo: float
float -> Número com ponto flutuante
O tipo float representa qualquer número 
positivo ou negativo com ponto flutuante
float sem sinal é considerado positivo
"""

print(10.5) # float positivo
print(-10.5) # negativo

""" 
A função type mostra o
tipo que o Python inferio ao valor
"""

print(type('')) # str (string)
print(type(10)) # int
print(type(10.5)) # float