""" COERÇÃO DE TIPOS """

""" 
Conversão de tipos, coerção,
type convertion, typecasting, coercion.
É o ato de converter um tipo em outros tipos
imutáveis e primitivos: str, int, float, bool
"""

print(1+1) # 2
print('a' + 'b') # ab

print(int('1'), type(int('1'))) # 1 <class 'int'>
print(float('1') + 1) # 2.0

""" CONVERSÃO DE TIPOS """

print(str(10) + 'a') # 10a
print(int('10') + 10) # 20
