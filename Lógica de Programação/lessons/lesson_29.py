""" OPERADORES DE ATRIBUIÇÃO """

"""
Operadores de Atribuição com Operadores Aritméticos

+ | += | -= | *= | /= | //= | **= | %=
"""

counter = 10

""" counter += 5
print(counter) """

""" counter -= 5
print(counter) """

""" counter *= 5
print(counter)  """

""" counter **= 5
print(counter) """

""" counter /= 5
print(counter) """

""" counter //= 5
print(counter) """

counter %= 5
print(counter)